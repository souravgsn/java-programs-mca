public class ap3 {
    public static void main(String[] args) {
        //int to byte
        int n = 10;
        byte x = (byte)n;
        System.out.println(x);
        
        
        //doublt to int 
        double d = 5.75;
        int e = (int)d;
        System.out.println(e);

        //double to byte
        double d2 = 6.99;
        byte b2 = (byte)d2;
        System.out.println(b2);

        //int to char;
        int a=65;  
        char c=(char)a;  
        System.out.println(c);  

        //float to short
        float f = 2.23451f;
        short s = (short) (f);
        System.out.println(s);
    }
}
